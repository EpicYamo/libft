#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	size_t	sizeofs1;
	char	*copy;
	size_t	i;

	i = 0;
	sizeofs1 = ft_strlen(s1);
	copy = (char *)malloc(sizeofs1 + 1);
	if (!copy)
		return (NULL);
	while (s1[i])
	{
		copy[i] = s1[i];
		i++;
	}
	copy[i] = 0;
	return (copy);
}
